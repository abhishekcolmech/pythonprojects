# Create an array using the method 1. array 2. arange 3. linspace

import numpy as np

# Using array

a = np.array([1, 2, 3])

print("Using array: ", a)
print(a[1])

# Using arrange

b = np.arange(0, 40, 10)

print("Using arrange: ", b)

# Using linspace

c = np.linspace(0, 1, 6)

print("Using linspace: ", c)